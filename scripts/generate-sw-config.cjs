#!/usr/bin/env node
/**
 * Generate firebase-messaging-sw.js with config from environment variables
 * This prevents committing sensitive Firebase config to git
 */

const fs = require('fs');
const path = require('path');

// Load environment variables from .env file
require('dotenv').config();

const firebaseConfig = {
  apiKey: process.env.VITE_FIREBASE_API_KEY,
  authDomain: process.env.VITE_FIREBASE_AUTH_DOMAIN,
  projectId: process.env.VITE_FIREBASE_PROJECT_ID,
  storageBucket: process.env.VITE_FIREBASE_STORAGE_BUCKET,
  messagingSenderId: process.env.VITE_FIREBASE_MESSAGING_SENDER_ID,
  appId: process.env.VITE_FIREBASE_APP_ID
};

// Validate that all required config values are present
const missingVars = Object.entries(firebaseConfig)
  .filter(([_, value]) => !value)
  .map(([key]) => key);

if (missingVars.length > 0) {
  console.error('❌ Missing required environment variables:');
  missingVars.forEach(key => console.error(`   - VITE_${key.replace(/([A-Z])/g, '_$1').toUpperCase()}`));
  console.error('\nPlease ensure all Firebase config variables are set in your .env file');
  process.exit(1);
}

// Service worker template
const swTemplate = `// Firebase Messaging Service Worker
// This handles push notifications when the app is in the background or closed
// ⚠️ This file is auto-generated by scripts/generate-sw-config.js
// DO NOT EDIT MANUALLY - Changes will be overwritten

// Import Firebase scripts for service worker
importScripts('https://www.gstatic.com/firebasejs/10.13.0/firebase-app-compat.js');
importScripts('https://www.gstatic.com/firebasejs/10.13.0/firebase-messaging-compat.js');

// Initialize Firebase in the service worker
// Config is injected from environment variables during build
const firebaseConfig = ${JSON.stringify(firebaseConfig, null, 2)};

firebase.initializeApp(firebaseConfig);

const messaging = firebase.messaging();

// Handle background messages
messaging.onBackgroundMessage((payload) => {
  console.log('[firebase-messaging-sw.js] Received background message:', payload);

  // Customize notification here
  const notificationTitle = payload.notification?.title || 'New message';
  const notificationOptions = {
    body: payload.notification?.body || 'You have a new message',
    icon: payload.notification?.icon || '/favicon.ico',
    badge: '/favicon.ico',
    tag: payload.data?.conversationId || 'default', // Prevents duplicate notifications
    data: {
      url: payload.data?.conversationId 
        ? \`/?chat=\${payload.data.conversationId}\` 
        : '/',
      ...payload.data
    },
    requireInteraction: false, // Auto-dismiss after a few seconds
    vibrate: [200, 100, 200], // Vibration pattern
  };

  return self.registration.showNotification(notificationTitle, notificationOptions);
});

// Handle notification click
self.addEventListener('notificationclick', (event) => {
  console.log('[firebase-messaging-sw.js] Notification clicked:', event.notification);
  
  event.notification.close();

  // Get the URL to open from notification data
  const urlToOpen = event.notification.data?.url || '/';

  // Focus or open the app window
  event.waitUntil(
    clients.matchAll({ type: 'window', includeUncontrolled: true })
      .then((clientList) => {
        // Check if there's already a window open
        for (const client of clientList) {
          if (client.url.includes(self.location.origin) && 'focus' in client) {
            return client.focus().then(() => {
              // Navigate to the specific URL
              return client.navigate(urlToOpen);
            });
          }
        }
        
        // If no window is open, open a new one
        if (clients.openWindow) {
          return clients.openWindow(urlToOpen);
        }
      })
  );
});
`;

// Write to public directory
const outputPath = path.join(__dirname, '..', 'public', 'firebase-messaging-sw.js');
fs.writeFileSync(outputPath, swTemplate, 'utf8');

console.log('✅ Generated firebase-messaging-sw.js successfully');
console.log(`   Output: ${outputPath}`);
console.log('   Config source: Environment variables from .env');
